package miscapp.options;

import miscapp.Main;

public class WindowResolution{
    
    public double newWidth;
    public double newHeight;
    
    public void res1920x1080(){
        this.newWidth = 1920;
        this.newHeight = 1080;
    }
    
    public void res800x600(){
        this.newWidth = 800;
        this.newHeight = 600;
    }
}
